        <?php include __DIR__ . '/../includes/header_user.php'; ?>

        <div class="page-header">
            <h1>My Payments</h1>
            <p>Review posted payments and download receipt copies.</p>
        </div>

        <?php if ($portalError !== ''): ?>
            <section class="section-card">
                <div class="alert-inline"><?= h($portalError) ?></div>
            </section>
        <?php endif; ?>

        <section class="section-card">
            <div class="sub-head">
                <h3 class="heading-reset">Payment History</h3>
                <span><?= number_format(count($payments)) ?> records</span>
            </div>
            <div class="table-wrap">
                <table class="notion-table">
                    <thead>
                    <tr>
                        <th>Payment Date</th>
                        <th>Invoice No</th>
                        <th>Amount</th>
                        <th>Payment Method</th>
                        <th>Reference No</th>
                        <th>Notes</th>
                        <th>Actions</th>
                    </tr>
                    </thead>
                    <tbody>
                    <?php if (!$payments): ?>
                        <tr><td colspan="7">No payments available.</td></tr>
                    <?php else: ?>
                        <?php foreach ($payments as $row): ?>
                            <tr>
                                <td><?= h((string)$row['payment_date']) ?></td>
                                <td><?= h((string)$row['invoice_no']) ?></td>
                                <td><?= peso($row['amount']) ?></td>
                                <td><?= h((string)($row['payment_method'] ?: 'N/A')) ?></td>
                                <td><?= h((string)($row['reference_no'] ?: 'N/A')) ?></td>
                                <td><?= h((string)($row['notes'] ?: '-')) ?></td>
                                <td class="table-actions">
                                    <a class="btn-link" href="/FinancialSM/user/payments.php?view=<?= (int)$row['id'] ?>">View</a>
                                    <a class="btn-link" href="/FinancialSM/user/payments.php?receipt_id=<?= (int)$row['id'] ?>&download=1">Download Receipt</a>
                                </td>
                            </tr>
                        <?php endforeach; ?>
                    <?php endif; ?>
                    </tbody>
                </table>
            </div>
        </section>

        <?php if ($paymentDetail): ?>
            <?php $orNo = (string)($paymentDetail['reference_no'] ?: ('OR-' . str_pad((string)$paymentDetail['id'], 5, '0', STR_PAD_LEFT))); ?>
            <section class="section-card">
                <div class="sub-head">
                    <h3 class="heading-reset">Payment Details</h3>
                    <span><a class="back-link" href="/FinancialSM/user/payments.php?receipt_id=<?= (int)$paymentDetail['id'] ?>&download=1">Download Receipt</a></span>
                </div>

                <div class="payment-props user-meta-grid">
                    <div><span>OR Number</span><strong><?= h($orNo) ?></strong></div>
                    <div><span>Payment Date</span><strong><?= h((string)$paymentDetail['payment_date']) ?></strong></div>
                    <div><span>Invoice No</span><strong><?= h((string)$paymentDetail['invoice_no']) ?></strong></div>
                    <div><span>Amount</span><strong><?= peso($paymentDetail['amount']) ?></strong></div>
                    <div><span>Payment Method</span><strong><?= h((string)($paymentDetail['payment_method'] ?: 'N/A')) ?></strong></div>
                    <div><span>Reference No</span><strong><?= h((string)($paymentDetail['reference_no'] ?: 'N/A')) ?></strong></div>
                </div>

                <div class="receipt-preview">
                    <p><strong>Notes:</strong> <?= h((string)($paymentDetail['notes'] ?: 'No notes provided.')) ?></p>
                    <div class="form-actions">
                        <button type="button" class="btn primary" onclick="window.print()">Print Receipt</button>
                    </div>
                </div>
            </section>
        <?php endif; ?>
    </main>
</div>
